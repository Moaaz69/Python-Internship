#Student Record System with Tuples & Sets

# Store student IDs using a tuple
student_ids = (10, 11, 12, 13)
print("Student IDs (tuple):", student_ids)

# Create a set of unique course names
courses = {"Python", "AI", "ML"}
print("Initial courses (set):", courses)

# Add a new course to the set
courses.add("Data Science")
print("After adding 'Data Science':", courses)

# Removing  a course from the set
courses.remove("AI")
print("After removing 'AI':", courses)

# Adding a course
courses.add("Deep Learning")
print("After adding 'Deep Learning':", courses)

